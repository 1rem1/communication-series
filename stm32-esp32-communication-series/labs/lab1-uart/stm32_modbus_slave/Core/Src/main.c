uint16_t Modbus_CRC16(uint8_t *data, uint16_t length) {
    uint16_t crc = 0xFFFF;
    for (uint16_t pos = 0; pos < length; pos++) {
        crc ^= (uint16_t)data[pos]; // XOR byte into least sig. byte of crc
        for (int i = 8; i != 0; i--) { // Loop over each bit
            if ((crc & 0x0001) != 0) { // If the LSB is set
                crc >>= 1; // Shift right
                crc ^= 0xA001; // XOR with polynomial
            } else {
                crc >>= 1; // Just shift right
            }
        }
    }
    return crc;
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
    if (huart->Instance == USART1) {
      if (rx_index == 0 && rx_data != 0x01) {
        HAL_UART_Receive_IT(&huart1, &rx_data, 1); // Restart UART reception in interrupt mode
        return; // Wait for the first byte to be the expected slave ID (0x01
      }
      rx_buffer[rx_index++] = rx_data; // Store received byte in buffer
      if (rx_index >= sizeof(rx_buffer)) {
        if (rx_buffer[0] == 0x01 && rx_buffer[1] == 0x05) { // Check for Modbus Write Coil command
            uint16_t calc_crc = Modbus_CRC16(rx_buffer, 6); // Calculate CRC for first 6 bytes
            uint16_t received_crc = rx_buffer[6] | (rx_buffer[7] << 8); // Extract received CRC

            if (calc_crc == received_crc) {
                // Valid Modbus command received, process it
                uint8_t slave_id = rx_buffer[0];
                uint16_t coil_address = (rx_buffer[2] << 8) | rx_buffer[3];
                uint8_t coil_value = rx_buffer[4] == 0xFF ? 1 : 0; // ON if 0xFF, OFF if 0x00

                // Here you can add code to handle the coil value (e.g., toggle an LED)
                if (coil_value) {
                    HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_RESET); // Turn ON LED
                } else {
                    HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_SET); // Turn OFF LED
                }
            }
            HAL_UART_Transmit(&huart1, rx_buffer, sizeof(rx_buffer), HAL_MAX_DELAY); // Echo back received frame
        }
        rx_index = 0; // Reset index if buffer is full
      }
    }
    HAL_UART_Receive_IT(&huart1, &rx_data, 1); // Restart UART reception in interrupt mode
}
